class ProxyHealth:
    def __init__(self):
        self.error_count: Dict[str, int] = defaultdict(int)
        self.success_count: Dict[str, int] = defaultdict(int)
        self.cooldown_until: Dict[str, float] = {}

    def can_use(self, proxy: str) -> bool:
        now = time.time()
        return proxy not in self.cooldown_until or now >= self.cooldown_until[proxy]

    def record(self, proxy: str, success: bool):
        if success:
            self.success_count[proxy] += 1
            self.error_count[proxy] = max(0, self.error_count[proxy] - 2)
        else:
            self.error_count[proxy] += 1
            if self.error_count[proxy] >= 8:   # Lower threshold = faster reuse
                self.cooldown_until[proxy] = time.time() + 20


